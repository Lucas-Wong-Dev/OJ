// 马虎的算式
/*
小明是个急性子，上小学的时候经常把老师写在黑板上的题目抄错了。有一次，老师出的题目是 36 x 495 = ?
他却给抄成了，396 x 45 = ?
但结果却很戏剧性，他的答案竟然是对的
因为 36 * 495 = 396 * 45 = 17820
类似这样的巧合情况可能还有很多，比如，27 * 594 = 297 * 54
假设 a b c d e 代表 1~9 不同的 5 个数字，注意是各不相同的数字，且不含 0
能满足形如， ab * cde = adb * ce 这样的算式一共有多少种呢况
输出： 142 种
*/

#include <stdio.h>
int main()
{
    int a, b, c, d, e;
    int cnt = 0; // 用于记录满足指定条件的算式的个数
    for (a = 1; a <= 9; a++)
        for (b = 1; b <= 9; b++)
            for (c = 1; c <= 9; c++)
                for (d = 1; d <= 9; d++)
                    for (e = 1; e <= 9; e++)
                        // ab*cde==adb*ce
                        if (
                            (10 * a + b) * (100 * c + 10 * d + e) == (a * 100 + d * 10 + b) * (c * 10 + e) &&
                            a != b &&
                            a != c &&
                            a != d &&
                            a != e &&
                            b != c &&
                            b != d &&
                            b != e &&
                            c != d &&
                            c != e &&
                            d != e)
                        {
                            cnt++;
                        }
    printf("%d\n", cnt);
    return 0;
}
